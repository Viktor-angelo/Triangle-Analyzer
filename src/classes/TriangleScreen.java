    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        // TODO add your handling code here:
        int a = sliA.getValue();
        int b = sliB.getValue();
        int c = sliC.getValue();
      if (a<b+c && b<a+c && c<a+b) {
          lblForma.setText("FORMS A TRIANGLE");
            if (a ==b && b ==c){
            lblTipo.setText("EQUILATERal");
                } else if (a !=b && b != c && c!=a ){
                    lblTipo.setText("SCALENE");}
                else {
                        lblTipo.setText("ISSOCILES");
        
                   
                        
                    }
                }
            
        else {
          lblForma.setText("DOES NOT FORM A TRIANGLE");
          lblTipo.setText("-------");}
      
        panResposta.setVisible(true);
    }//GEN-LAST:event_btnCalculateActionPerformed

